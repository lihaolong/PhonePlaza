package model;

public class CameraInfo {
	private String cameraName;
	private String cameraPx;
	private float sperture;
	private boolean supportois;

	public String getCameraName() {
		return cameraName;
	}

	public void setCameraName(String cameraName) {
		this.cameraName = cameraName;
	}

	public String getCameraPx() {
		return cameraPx;
	}

	public void setCameraPx(String cameraPx) {
		this.cameraPx = cameraPx;
	}

	public float getSperture() {
		return sperture;
	}

	public void setSperture(float sperture) {
		this.sperture = sperture;
	}

	public boolean isSupportois() {
		return supportois;
	}

	public void setSupportois(boolean supportois) {
		this.supportois = supportois;
	}

}
